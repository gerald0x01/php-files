<?php 

$number_one = 10;
$number_two = 20;

// $number_one = $number_one + $number_two;
$number_one += $number_two;
echo $number_one; // 30

$number_one -= $number_two;
echo $number_one; // -10

$number_one *= $number_two;
echo $number_one; // 200

$number_one /= $number_two;
echo $number_one; // 0.2

$number_one %= $number_two;
echo $number_one; // 0


